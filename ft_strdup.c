#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;	
	int		i;

	i = ft_strlen(s1);
	s = malloc(i + 1);
	if (s == 0)
		return (0);
	ft_strlcpy(s, s1, i + 1);
	return (s);
}
